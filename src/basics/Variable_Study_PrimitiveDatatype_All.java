package basics;

public class Variable_Study_PrimitiveDatatype_All {

	public static void main(String[] args) {
		
		//Variable Declaration//
		byte Rollnumber;
		//Variable Initialization//
		Rollnumber=16;
		//Usage//
		System.out.println("My roll number is "+Rollnumber);		

		//Variable Declaration//
		short Ranking;
		//Variable Initialization//
		Ranking=12456;
		//Usage//
		System.out.println("My AIEEE Ranking is "+Ranking);
		
		//Variable Declaration//
		int Valuation;
		//Variable Initialization//
		Valuation=1234567890;
		//Usage//
		System.out.println("My company's Valuation is "+Valuation);
		
		//Variable Declaration//
		long Population;
		//Variable Initialization//
		Population=7400000l;
		//Usage//
		System.out.println("Population of Pune is "+Population);
		
		//Variable Declaration//
		float Marks;
		//Variable Initialization//
		Marks=67.8999f;
		//Usage//
		System.out.println("My marks are "+Marks);
		
		//Variable Declaration//
		double HomeloanInterest;
		//Variable Initialization//
		HomeloanInterest=6.5999d;
		//Usage//
		System.out.println("Interest for Home Loan is "+HomeloanInterest);
		
		//Variable Declaration//
		char Grade;
		//Variable Initialization//
		Grade='A';
		//Usage//
		System.out.println("My Grade for 12th exam is "+Grade);
		
		//Variable Declaration//
		boolean Statement;
		//Variable Initialization//
		Statement=true;
		//Usage//
		System.out.println("The statement he said is "+Statement);
		
		
		
		
	}

}
