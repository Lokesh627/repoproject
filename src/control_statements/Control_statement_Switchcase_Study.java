package control_statements;

public class Control_statement_Switchcase_Study {

	public static void main(String[] args) 
	{	//You are Engineering Student//
		//1-->First year engineering//
		//2-->Second year engineering//
		//3-->Third year engineering//
		//4-->Fourth year engineering//
		//Invalid Year//
		
		int Year=5;
		
		switch (Year)
		{
		case 1:System.out.println("First year engineering");
		break;
		
		case 2:System.out.println("Second year engineering");
		break;
		
		case 3:System.out.println("Third year engineering");
		break;
		
		case 4:System.out.println("Fourth year engineering");
		break;
		
		default:System.out.println("Invalid Year");
		break;
		
		}
		

	}

}
